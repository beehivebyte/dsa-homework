#include<iostream>
using namespace std;

int main() {
    int a;

    // Approach:
    // Read the integer from input.
    // Print the same integer as output without modification.
    // Do not print anything before taking input
    cin >> a;
// Output the value of a
    cout << a << endl;

    return 0;
}


// Time Complexity:
// Best: O(1)
// Average: O(1)
// Worst: O(1)
// Space Complexity: O(1)
